// Helper untuk mencari Post berdasarkan ID di seluruh data
export const findPostById = (postId) => {
    const id = parseInt(postId); 
    for (const umkm of umkmData) {
        if (umkm.posts && Array.isArray(umkm.posts)) {
            const foundPost = umkm.posts.find(p => p.id === id);
            if (foundPost) {
                return { 
                    ...foundPost, 
                    umkmData: umkm 
                }; 
            }
        }
    }
    return null;
};

// Helper untuk cek jam buka
export const checkOpenStatus = (hours) => {
    if (!hours) return false;
    const now = new Date();
    const currentHour = now.getHours();
    if (hours.open === 0 && hours.close === 24) return true;
    return currentHour >= hours.open && currentHour < hours.close;
};